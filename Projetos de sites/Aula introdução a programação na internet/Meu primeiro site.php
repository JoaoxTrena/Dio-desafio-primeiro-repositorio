O azul costuma estar associado à frieza, depressão, monotonia. Por isso mesmo, também à paz, à ordem, à harmonia. Entre os matizes, é o menos expansivo e forte aos olhos, sendo a cor mais fria. Sinônimos: cerúleo, cárdeo, celeste, azulado, safira. Também há relação à estimulação da criatividade e tranquilidade. É a cor favorita de 45% das pessoas do mundo, possuindo 111 tons diferentes nomeados. Principais tonalidades de azul: azul-bebê, azul-celeste, azul-cobalto, azul-marinho, azul-turquesa.
